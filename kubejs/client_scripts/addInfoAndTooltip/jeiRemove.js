let getCorrectItem = (emiBannedItems) => {
    let clean = emiBannedItems.replace('item:', '');
    
    if (clean.includes('{')) {
        let parts = clean.split('{');
        let id = parts[0];
        let nbt = '{' + parts[1];
        
        return Item.of(id, nbt);
    }
    
    return clean;
};

JEIEvents.hideItems((event) => {

    emiBannedItems.forEach(item => {
        event.hide(getCorrectItem(item))
    });

    event.hide('enderio:filled_soul_vial')
    event.hide('enderio:broken_spawner')
    event.hide('/^enderio:.*clear_glass.*/')
    event.hide('/^enderio:.*fused_quartz.*/')

})